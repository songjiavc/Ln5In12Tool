package com.baiylin.songjia.ln5in12tool.bean;

public class GroupBean {
	
	private String group;

	private int todayCount;

	private int yestedayCount;

	private int lastTwoDayCount;

	private int lastThreeDayCount;

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public int getTodayCount() {
		return todayCount;
	}

	public void setTodayCount(int todayCount) {
		this.todayCount = todayCount;
	}

	public int getYestedayCount() {
		return yestedayCount;
	}

	public void setYestedayCount(int yestedayCount) {
		this.yestedayCount = yestedayCount;
	}

	public int getLastTwoDayCount() {
		return lastTwoDayCount;
	}

	public void setLastTwoDayCount(int lastTwoDayCount) {
		this.lastTwoDayCount = lastTwoDayCount;
	}

	public int getLastThreeDayCount() {
		return lastThreeDayCount;
	}

	public void setLastThreeDayCount(int lastThreeDayCount) {
		this.lastThreeDayCount = lastThreeDayCount;
	}
}
